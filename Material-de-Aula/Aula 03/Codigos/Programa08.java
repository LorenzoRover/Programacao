package exemplos;

public class Programa08 {

	public static void main(String[] args) {
		
			for (int i = 0; i <= 10; i++) {
				
				System.out.print(i + " ");
				if(i == 5) {
					System.out.println("5 encontrado!!!");
					break;
				}				
			}
			
			int i = 0;
			while (i <= 10) {
				System.out.print(i + " ");
				i++;
			}
			
			
			
	}

}
